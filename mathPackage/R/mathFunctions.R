#' My Sum
#'
#' Returns the sum of 2 numbers
#' @param x A numeric value to sum
#' @param y A numeric value to sum
#' @keywords add
#' @export
#' @examples
#' my_sum(4, 5)

my_sum <- function(x, y) {
  return(x + y)
}

#' My Multiplication
#'
#' Returns the product of 2 numbers
#' @param x A numeric value to multiply
#' @param y A numeric value to multiply
#' @keywords multiply product
#' @export
#' @examples
#' my_mult(4, 5)

my_mult <- function(x, y) {
  return(x * y)
}
